"""
Auto-generates a graph-grounded evaluation question set from data/graph.json.

Why: the hand-written data/eval_questions.json targets famous papers a small
model already knows, so RAG / A2 / A3 have nothing to add — the ON and OFF
answers come out byte-identical and every judge verdict is a vacuous tie. This
generates questions about the *obscure, recent* papers in the graph (entities a
small model has NOT memorized), so retrieval and the control surfaces become
measurable. Every question is answerable from the graph (paper abstract +
author/topic neighbours), and 'entities' lists canonical strings (author
surnames, topic names) for the Phase-1 string metrics (A2 entity fidelity).

Recent/obscure papers are preferred (sorted by year, newest first), and the
famous foundational anchors are skipped so the set is genuinely out-of-domain.

Usage:
  python scripts/make_eval_questions.py --graph data/graph.json --out data/eval_questions_ood.json --n 40
"""
import json, argparse, re
from collections import defaultdict

# Famous anchor papers (see fetch_arxiv.FOUNDATIONAL_IDS) — skip these so the
# generated set targets entities the model has not memorized.
FOUNDATIONAL_ARXIV = {
    "1706.03762", "1810.04805", "2005.14165", "2106.09685", "2205.14135",
    "1301.3666", "1607.06450", "1512.03385", "2010.11929", "2203.15556",
    "2302.13971", "2310.06825", "2307.09288", "2210.11610", "2112.10752",
    "1910.10683", "2103.00020", "2204.05149", "1901.00596", "2108.12409",
}


def surname(name):
    """Last capitalized token >= 4 chars — the form the trie guard (A2) can score."""
    parts = [w for w in re.split(r"\s+", name.replace(".", "")) if len(w) >= 4 and w[:1].isupper()]
    return parts[-1] if parts else None


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--graph", default="data/graph.json")
    ap.add_argument("--out", default="data/eval_questions_ood.json")
    ap.add_argument("--n", type=int, default=40, help="max questions to emit")
    ap.add_argument("--model", default="liquid/lfm-2.5-1.2b-instruct:free")
    args = ap.parse_args()

    graph = json.loads(open(args.graph, encoding="utf-8").read())
    nodes = {n["id"]: n for n in graph["nodes"]}

    authored_by = defaultdict(list)  # paper id -> [author names]
    topics_of = defaultdict(list)    # paper id -> [topic names]
    for e in graph["edges"]:
        src, dst, rel = nodes.get(e["from"]), nodes.get(e["to"]), e["rel"]
        if not src or not dst:
            continue
        if rel == "authored" and src["type"] == "author" and dst["type"] == "paper":
            authored_by[dst["id"]].append(src["name"])
        elif rel == "about" and src["type"] == "paper" and dst["type"] == "topic":
            topics_of[src["id"]].append(dst["name"])

    papers = [n for n in graph["nodes"] if n["type"] == "paper"]
    # Out-of-domain first: drop the famous anchors, then newest papers first.
    papers = [p for p in papers if p.get("props", {}).get("arxiv") not in FOUNDATIONAL_ARXIV]
    papers.sort(key=lambda p: p.get("props", {}).get("year") or 0, reverse=True)

    questions = []
    for p in papers:
        title = p["name"]
        authors = authored_by.get(p["id"], [])
        abstract = (p.get("props", {}) or {}).get("abstract", "")
        snames = [s for s in (surname(a) for a in authors) if s]

        # Authorship question — exercises A2 (obscure surname spelling) + RAG.
        if snames:
            questions.append({
                "id": f"ood_auth_{len(questions):03d}",
                "text": f'Who are the authors of the paper titled "{title}"?',
                "entities": snames[:5],
            })
        # Contribution question — exercises RAG groundedness on an unseen abstract.
        if abstract:
            questions.append({
                "id": f"ood_about_{len(questions):03d}",
                "text": f'According to the knowledge graph, what problem does the paper "{title}" address or what does it propose?',
                "entities": topics_of.get(p["id"], [])[:2],
            })
        if len(questions) >= args.n:
            break

    questions = questions[:args.n]
    out = {
        "_comment": "Auto-generated by scripts/make_eval_questions.py from the graph. Targets obscure/recent papers so RAG/A2/A3 are measurable. Regenerate after re-ingesting.",
        "model": args.model,
        "questions": questions,
    }
    with open(args.out, "w", encoding="utf-8") as f:
        json.dump(out, f, indent=2, ensure_ascii=False)
    print(f"Wrote {len(questions)} questions to {args.out} "
          f"({sum(1 for q in questions if q['id'].startswith('ood_auth'))} authorship, "
          f"{sum(1 for q in questions if q['id'].startswith('ood_about'))} contribution)")


if __name__ == "__main__":
    main()
